#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*result;
	int		len;
	int		i;

	len = 0;
	while (s[len])
		len++;
	if (!(result = (char *)malloc(sizeof(char) * (len + 1))))
		return (0);
	i = 0;
	while (s[i])
	{
		result[i] = s[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
